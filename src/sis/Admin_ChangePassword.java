    //create a function to verify empty fields
    public boolean verifyFields()
    {
        String pass1 = String.valueOf(jPasswordField1.getPassword());
        String pass2 = String.valueOf(jPasswordField2.getPassword());
        String pass3 = String.valueOf(jPasswordField3.getPassword());
        
        //check empty fields
        if(pass1.trim().equals("") || pass2.trim().equals("") || pass3.trim().equals(""))
        {
            JOptionPane.showMessageDialog(null,"One or More fields are empty","Empty Fields",2);
            return false;
        }
        
        //check if two password fields are equal
        else if(!pass2.equals(pass3))
        {
            JOptionPane.showMessageDialog(null,"Password doesn't match","Confirm Password",2);
            return false;
        }
      
        //check length of password
        else if(pass2.length() < 5)
        {
            JOptionPane.showMessageDialog(null,"Too small","New Password",2);
            return false;
        }
        
        else if(pass2.length() > 18)
        {
            JOptionPane.showMessageDialog(null,"Too long","New Password",2);
            return false;
        }
        
        //if eveything is ok
        else{
            return true;
        }
    }
    
    //to check constraints
    public boolean checkPassword()
    {
        String pass2 = String.valueOf(jPasswordField2.getPassword());
        boolean hasNum =false;
        boolean hasCap =false;
        boolean hasLow =false;
        boolean hasSchar =false;
        char c;
        Pattern specialChar = Pattern.compile("[@!#]");
        if(specialChar.matcher(pass2).find())
            hasSchar = true;
        for(int i=0; i<pass2.length();i++)
        {
            c = pass2.charAt(i);
            if(Character.isDigit(c))
                hasNum = true;
            else if(Character.isUpperCase(c))
                hasCap = true;
            else if(Character.isLowerCase(c))
                hasLow = true;
            if(hasNum & hasCap & hasLow & hasSchar)
                return true;
        }
        return false;
    }
