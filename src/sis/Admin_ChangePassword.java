    private void jButton_ChangeActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_ChangeActionPerformed

        
        String pass1 = String.valueOf(jPasswordField1.getPassword());
        String pass2 = String.valueOf(jPasswordField2.getPassword());
        String pass3 = String.valueOf(jPasswordField3.getPassword());

        if(verifyFields())
        {
            if(checkPassword())
            {
                PreparedStatement ps;
                ResultSet rs;
            
                String query = "SELECT * FROM `admin` WHERE `admin_id` = ? AND `a_password` = ?";
            
                String UpdateQuery = "UPDATE `admin` SET `a_password`=? WHERE `admin_id`=?";

                try {
                    ps = My_CNX.getconnection().prepareStatement(query);

                    ps.setString(1, id);
                    ps.setString(2, pass1);
                    rs = ps.executeQuery();

                    if(rs.next())
                    {
                        ps = My_CNX.getconnection().prepareStatement(UpdateQuery);

                        ps.setString(1, pass2);
                        ps.setString(2, id);
                        if(ps.executeUpdate()!= 0){
                            JOptionPane.showMessageDialog(null, "Your password has been changed");
                            Admin_Portal form = new Admin_Portal(id);
                            form.setVisible(true);
                            form.pack();
                            form.setLocationRelativeTo(null);
                            this.dispose();
                        }
                        else{
                            JOptionPane.showMessageDialog(null, "Error: Check your information");
                        }
                    }
                    else{
                        JOptionPane.showMessageDialog(null, "Invalid Password", "Error",2);
                    }
                } catch (SQLException ex) {
                    Logger.getLogger(Admin_ChangePassword.class.getName()).log(Level.SEVERE, null, ex);
                }
            }
            else{
                JOptionPane.showMessageDialog(null, "Password must contain atleast one :- "
                        + "\n-> digit\n-> lowercase\n-> uppercase\n-> special character( @,!,# )", "New Password",2);
            }
        }
    }//GEN-LAST:event_jButton_ChangeActionPerformed

