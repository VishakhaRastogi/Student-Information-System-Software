    public Student_Profile(String id) {
        initComponents();
        
        this.id = id;
        //center the form
        this.setLocationRelativeTo(null);
        
        //create the border for jpanel_title
        Border title_border = BorderFactory.createMatteBorder(1, 1, 1, 1, Color.white);
        //set the border
        jPanel_title.setBorder(title_border);
        
        //create the border for close and minimize jlabels
        Border label_border = BorderFactory.createMatteBorder(1, 1, 1, 1, Color.black);
        jLabel_minimize.setBorder(label_border);
        jLabel_close.setBorder(label_border);
        
        //display     
        PreparedStatement ps;
        ResultSet rs;
        String UserQuery = "SELECT `student_id`, `s_name`, `father_name`, `course_name`, `branch`, `batch`, `section`, `address`, `phone`, `gender`, `nationality`, `email` FROM `student` WHERE `student_id`=?";

        try {
            ps = My_CNX.getconnection().prepareStatement(UserQuery);

            ps.setString(1, id);
            rs = ps.executeQuery();
            if(rs.next()){
                
                jTextField_Id.setText(rs.getString(1));
                jTextField_Fullname.setText(rs.getString(2));
                jTextField_Father.setText(rs.getString(3));
                jTextField_Dept.setText(rs.getString(4));
                jTextField_Branch.setText(rs.getString(5));
                jTextField_Batch.setText(rs.getString(6));
                jTextField_Section.setText(rs.getString(7));
                jTextArea_Address.setText(rs.getString(8));
                jTextField_Phone.setText(rs.getString(9));
                if(rs.getString(10)!= "NULL")
                    jComboBox_Gender.setSelectedItem(rs.getString(10));
                if(rs.getString(11)!= "NULL")
                    jComboBox_Nation.setSelectedItem(rs.getString(11));
                jTextField_Email.setText(rs.getString(12));
            }

        } catch (SQLException ex) {
            Logger.getLogger(Student_Profile.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

