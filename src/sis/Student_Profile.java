    private void jButton_UpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_UpdateActionPerformed

        String father = jTextField_Father.getText();
        String phone = jTextField_Phone.getText();
        String address = jTextArea_Address.getText();  
        String email = jTextField_Email.getText();
        String gender = (String) jComboBox_Gender.getSelectedItem();
        String nationality = (String) jComboBox_Nation.getSelectedItem();

        if(verifyFields())
        {
            PreparedStatement ps;
            ResultSet rs;
            String updateUserQuery = "UPDATE `student` SET `father_name`=?, `phone`=?,`email`=?,`address`=?, `gender`=?, `nationality`=? WHERE `student_id`=?";

            try {
                    ps = My_CNX.getconnection().prepareStatement(updateUserQuery);

                    ps.setString(1, father);
                    ps.setString(2, phone);
                    ps.setString(3, email);
                    ps.setString(4, address);
                    ps.setString(5, gender);
                    ps.setString(6, nationality);
                    ps.setString(7, id);
                    if(ps.executeUpdate()!= 0){
                        JOptionPane.showMessageDialog(null, "Your profile has been modified");
                        Student_Profile form = new Student_Profile(id);
                        form.setVisible(true);
                        form.pack();
                        form.setLocationRelativeTo(null);
                        this.dispose();
                    }
                    else{
                        JOptionPane.showMessageDialog(null, "Error: Check your information");
                    }

            } catch (SQLException ex) {
                Logger.getLogger(Student_Profile.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }//GEN-LAST:event_jButton_UpdateActionPerformed

