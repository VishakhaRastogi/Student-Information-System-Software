    private void jButton_RegisterActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_RegisterActionPerformed

        String fname = jTextField_Fullname.getText();
        String s_id = jTextField_Studentid.getText();
        String phone = jTextField_Phone.getText();
        String pass1 = String.valueOf(jPasswordField1.getPassword());
        String pass2 = String.valueOf(jPasswordField2.getPassword());
        String batch = (String) jComboBox_Batch.getSelectedItem();
        String section = (String) jComboBox_Section.getSelectedItem();
        
        if(verifyFields())
        {
            if(!checkStudentId(s_id))
            {
                PreparedStatement ps,st,st_res;
                ResultSet rs;
                String registerUserQuery = "INSERT INTO `student`(`student_id`, `s_password`,`s_name`, `phone`, `batch`,`section`) VALUES (?,?,?,?,?,?)";
                String query ="INSERT INTO `attendance`(`student_id`) VALUES (?)";
                String query_result ="INSERT INTO `result`(`student_id`) VALUES (?)";
                try {
                    ps = My_CNX.getconnection().prepareStatement(registerUserQuery);

                    ps.setString(1, s_id);
                    ps.setString(2, pass1);
                    ps.setString(3, fname);
                    ps.setString(4, phone);
                    ps.setString(5, batch);
                    ps.setString(6, section);
                    st = My_CNX.getconnection().prepareStatement(query);
                    st.setString(1, s_id);
                    st_res = My_CNX.getconnection().prepareStatement(query_result);
                    st_res.setString(1, s_id);
                    
                    if(ps.executeUpdate()!= 0){
                        JOptionPane.showMessageDialog(null, "Your account has been created");
                        Register_Student form = new Register_Student(id);
                        form.setVisible(true);
                        form.pack();
                        form.setLocationRelativeTo(null);
                        this.dispose();
                        st.executeUpdate();
                        st_res.executeUpdate();
                    }
                    else{
                        JOptionPane.showMessageDialog(null, "Error: Check your information");
                    }

                } catch (SQLException ex) {
                    Logger.getLogger(Register_Student.class.getName()).log(Level.SEVERE, null, ex);
                }
            }
        }
    }//GEN-LAST:event_jButton_RegisterActionPerformed

