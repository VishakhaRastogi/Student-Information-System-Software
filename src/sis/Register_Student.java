    public boolean checkStudentId(String s_id)
    {
        PreparedStatement st;
        ResultSet rs;
        boolean studentid_exist = false;
        
        String query = "SELECT * FROM `student` WHERE `student_id` = ?";
        
        try {
            st = My_CNX.getconnection().prepareStatement(query);
            st.setString(1, s_id);
            rs = st.executeQuery();
            
            if(rs.next())
            {
                studentid_exist =true;
                JOptionPane.showMessageDialog(null, "This Student Id is already registered, Choose another one", "Student Id Failed",2);
            }
            
        } catch (SQLException ex) {
            Logger.getLogger(Register_Student.class.getName()).log(Level.SEVERE, null, ex);
        }
        
        return studentid_exist;
    }
    
    //to check constraints
    public boolean checkPassword()
    {
        String pass1 = String.valueOf(jPasswordField1.getPassword());
        boolean hasNum =false;
        boolean hasCap =false;
        boolean hasLow =false;
        boolean hasSchar =false;
        char c;
        Pattern specialChar = Pattern.compile("[@!#]");
        if(specialChar.matcher(pass1).find())
            hasSchar = true;
        for(int i=0; i<pass1.length();i++)
        {
            c = pass1.charAt(i);
            if(Character.isDigit(c))
                hasNum = true;
            else if(Character.isUpperCase(c))
                hasCap = true;
            else if(Character.isLowerCase(c))
                hasLow = true;
            if(hasNum & hasCap & hasLow & hasSchar)
                return true;
        }
        return false;
    }
